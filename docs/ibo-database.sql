          'created_at', p.created_at,
          'updated_at', p.updated_at,
          'user_count', (SELECT COUNT(*) FROM public.ibo_user_subscriptions s WHERE s.package_id = p.id),
          'device_count', (SELECT COUNT(*) FROM public.ibo_devices d WHERE d.package_id = p.id),
          'live_stream_ids', COALESCE((SELECT jsonb_agg(m.live_stream_id ORDER BY m.live_stream_id) FROM public.ibo_package_live_streams m WHERE m.package_id = p.id), '[]'::jsonb),
          'vod_stream_ids', COALESCE((SELECT jsonb_agg(m.vod_stream_id ORDER BY m.vod_stream_id) FROM public.ibo_package_vod_streams m WHERE m.package_id = p.id), '[]'::jsonb),
          'series_ids', COALESCE((SELECT jsonb_agg(m.series_id ORDER BY m.series_id) FROM public.ibo_package_series m WHERE m.package_id = p.id), '[]'::jsonb)
        )
        ORDER BY p.created_at DESC
      )
      FROM public.ibo_packages p
    ), '[]'::jsonb),
    'users', COALESCE((
      SELECT jsonb_agg(
        jsonb_build_object(
          'id', u.id,
          'username', u.username,
          'status', CASE WHEN u.status = 'Banned' THEN 'Blocked' ELSE u.status END,
          'start_date', u.start_date,
          'expiry_date', u.expiry_date,
          'max_connections', u.max_connections,
          'notes', u.notes,
          'created_at', u.created_at,
          'subscription_id', s.id,
          'subscription_status', COALESCE(s.status, CASE WHEN u.status = 'Banned' THEN 'Blocked' ELSE u.status END),
          'package_id', s.package_id,
          'package_name', p.name,
          'subscription_expiry_date', COALESCE(s.expiry_date, u.expiry_date),
          'subscription_notes', s.notes,
          'device_count', (SELECT COUNT(*) FROM public.ibo_devices d WHERE d.user_id = u.id),
          'playlist_url', ip.public_url,
          'playlist_generated_at', ip.generated_at,
          'playlist_channel_count', ip.channel_count
        )
        ORDER BY u.created_at DESC
      )
      FROM public.iptv_users u
      LEFT JOIN public.ibo_user_subscriptions s ON s.user_id = u.id
      LEFT JOIN public.ibo_packages p ON p.id = s.package_id
      LEFT JOIN public.ibo_playlists ip ON ip.user_id = u.id
    ), '[]'::jsonb),
    'subscriptions', COALESCE((
      SELECT jsonb_agg(
        jsonb_build_object(
          'id', s.id,
          'user_id', s.user_id,
          'username', u.username,
          'package_id', s.package_id,
          'package_name', p.name,
          'status', s.status,
          'expiry_date', s.expiry_date,
          'max_connections', s.max_connections,
          'notes', s.notes,
          'created_at', s.created_at,
          'updated_at', s.updated_at
        )
        ORDER BY s.updated_at DESC
      )
      FROM public.ibo_user_subscriptions s
      LEFT JOIN public.iptv_users u ON u.id = s.user_id
      LEFT JOIN public.ibo_packages p ON p.id = s.package_id
    ), '[]'::jsonb),
    'playlists', COALESCE((
      SELECT jsonb_agg(
        jsonb_build_object(
          'id', p.id,
          'user_id', p.user_id,
          'username', p.username,
          'bucket_name', p.bucket_name,
          'storage_path', p.storage_path,
          'public_url', p.public_url,
          'channel_count', p.channel_count,
          'generated_at', p.generated_at,
          'updated_at', p.updated_at
        )
        ORDER BY p.generated_at DESC
      )
      FROM public.ibo_playlists p
    ), '[]'::jsonb),
    'settings', jsonb_build_object(
      'app_name', COALESCE(settings_map->>'app_name', 'OMAR IBO PANEL'),
      'logo_url', COALESCE(settings_map->>'logo_url', ''),
      'api_base_url', COALESCE(settings_map->>'api_base_url', ''),
      'default_epg_url', COALESCE(settings_map->>'default_epg_url', ''),
      'theme_mode', COALESCE(settings_map->>'theme_mode', 'system'),
      'primary_color', COALESCE(settings_map->>'primary_color', '#6D4CFF'),
      'secondary_color', COALESCE(settings_map->>'secondary_color', '#1ec8a5'),
      'maintenance_mode', COALESCE((settings_map->>'maintenance_mode')::boolean, false),
      'notifications_enabled', COALESCE((settings_map->>'notifications_enabled')::boolean, true)
    ),
    'logs', COALESCE((
      SELECT jsonb_agg(
        jsonb_build_object(
          'id', l.id,
          'action', l.action,
          'entity_type', l.entity_type,
          'entity_id', l.entity_id,
          'message', l.message,
          'created_at', l.created_at
        )
        ORDER BY l.created_at DESC
      )
      FROM (
        SELECT * FROM public.ibo_activity_logs ORDER BY created_at DESC LIMIT 20
      ) l
    ), '[]'::jsonb),
    'content', jsonb_build_object(
      'live', COALESCE((
        SELECT jsonb_agg(
          jsonb_build_object(
            'id', s.id,
            'name', s.name,
            'logo_url', s.logo_url,
            'category_name', COALESCE(c.name, 'Live TV')
          )
          ORDER BY s.sort_order ASC, s.id ASC
        )
        FROM public.live_streams s
        LEFT JOIN public.live_categories c ON c.id = s.category_id
        WHERE s.status = 'Active'
      ), '[]'::jsonb),
      'vod', COALESCE((
        SELECT jsonb_agg(
          jsonb_build_object(
            'id', v.id,
            'title', v.title,
            'poster_url', v.poster_url,
            'category_name', COALESCE(c.name, 'Movies')
          )
          ORDER BY v.sort_order ASC, v.id ASC
        )
        FROM public.vod_streams v
        LEFT JOIN public.vod_categories c ON c.id = v.category_id
        WHERE v.status = 'Active'
      ), '[]'::jsonb),
      'series', COALESCE((
        SELECT jsonb_agg(
          jsonb_build_object(
            'id', s.id,
            'title', s.title,
            'poster_url', s.poster_url,
            'category_name', COALESCE(c.name, 'Series')
          )
          ORDER BY s.sort_order ASC, s.id ASC
        )
        FROM public.series s
        LEFT JOIN public.series_categories c ON c.id = s.category_id
        WHERE s.status = 'Active'
      ), '[]'::jsonb)
    )
  );
END;
