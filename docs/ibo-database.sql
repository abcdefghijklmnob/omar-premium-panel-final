  SELECT COALESCE(jsonb_object_agg(key, value), '{}'::jsonb)
  INTO settings_map
  FROM public.ibo_settings;

  RETURN jsonb_build_object(
    'counts', jsonb_build_object(
      'devices', (SELECT COUNT(*) FROM public.ibo_devices),
      'active_devices', (SELECT COUNT(*) FROM public.ibo_devices WHERE status = 'Active'),
      'expired_devices', (SELECT COUNT(*) FROM public.ibo_devices WHERE status = 'Expired'),
      'blocked_devices', (SELECT COUNT(*) FROM public.ibo_devices WHERE status = 'Blocked'),
      'connected_users', (SELECT COUNT(DISTINCT user_id) FROM public.ibo_devices WHERE user_id IS NOT NULL AND status = 'Active'),
      'packages', (SELECT COUNT(*) FROM public.ibo_packages),
      'channels', (SELECT COUNT(*) FROM public.live_streams WHERE status = 'Active'),
      'movies', (SELECT COUNT(*) FROM public.vod_streams WHERE status = 'Active'),
      'series', (SELECT COUNT(*) FROM public.series WHERE status = 'Active')
